/*
O primeiro programa em Java ("Hello World!")

criação de uma classe responsável por printar uma mensagem na tela

*/

class MeuPrimeiroPrograma {
	public static void main(String[] args) { //método ponto-de-entrada (start)
		System.out.println("Meu primeiro programa em Java!"); //printa mensagem no console/tela

	}
}

/*

Para execução é necessário compilá-lo criando o arquivo .class
Instalar JDK/compilador javac
comandos: javac MeuPrimeiroPrograma.java (cria .class)
execução: java MeuPrimeiroPrograma

*/